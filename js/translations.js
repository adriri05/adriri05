function changeLanguage(lang) {
    document.documentElement.lang = lang;
    const elements = translations[lang];
    for (let key in elements) {
        const el = document.getElementById(key);
        if (el) {
            el.textContent = elements[key];
        }
    }
    // Update select value if it exists (for backward compatibility if select is used elsewhere)
    const switcher = document.getElementById('language-switcher');
    if (switcher) {
        switcher.value = lang;
    }
    
    // Update button styles for the new aesthetic switcher
    updateLanguageButtons(lang);

    // Save language preference
    localStorage.setItem('preferredLanguage', lang);
}

function updateLanguageButtons(lang) {
    const btnEn = document.getElementById('lang-btn-en');
    const btnEs = document.getElementById('lang-btn-es');
    
    if (btnEn && btnEs) {
        // Base classes common to both buttons - fully rounded
        const baseClass = "px-3 py-1.5 rounded-full text-xs font-bold transition-all duration-200";
        
        // Active vs Inactive styles
        const inactiveClass = "text-slate-500 hover:text-slate-900 dark:hover:text-white bg-transparent";
        const activeClass = "bg-primary text-white shadow-sm";

        if (lang === 'en') {
            btnEn.className = `${baseClass} ${activeClass}`;
            btnEs.className = `${baseClass} ${inactiveClass}`;
        } else {
            btnEs.className = `${baseClass} ${activeClass}`;
            btnEn.className = `${baseClass} ${inactiveClass}`;
        }
    }
}

// Load preferred language
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLanguage') || 'en';
    changeLanguage(savedLang);

    // Load preferred theme
    const savedTheme = localStorage.getItem('theme');
    if (savedTheme === 'light') {
        document.documentElement.classList.remove('dark');
    } else {
        document.documentElement.classList.add('dark');
    }
    updateThemeIcon();
});

function toggleTheme() {
    const html = document.documentElement;
    const isDark = html.classList.contains('dark');
    if (isDark) {
        html.classList.remove('dark');
        localStorage.setItem('theme', 'light');
    } else {
        html.classList.add('dark');
        localStorage.setItem('theme', 'dark');
    }
    updateThemeIcon();
}

function updateThemeIcon() {
    const icon = document.getElementById('theme-icon');
    if (icon) {
        const isDark = document.documentElement.classList.contains('dark');
        icon.textContent = isDark ? 'light_mode' : 'dark_mode';
    }
}
